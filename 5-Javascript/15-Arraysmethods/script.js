var students = ['asd', 'dqwfq', 'qwdfgww', 'wrghwr'];


// students[4] = 'Bernadette';
// students[5] = 'Johny';
// we can push method and adds a new item to the end of an array
students.push('Miladof');
students.push('Roro');
console.log(students);

var student_length = students.push('ASKJFHDGS');
console.log(student_length);
console.log(students);

// pop method removes the last item from array

students.pop();
console.log(students);

var lastStudent = students.pop();
console.log(lastStudent);

//unshift add new item to the beginning of array

var cats = ['cat1', 'cat2', 'cat3']
cats.unshift('cat4');
console.log(cats);

//shift method removes item from beginning

cats.shift();
console.log(cats);

